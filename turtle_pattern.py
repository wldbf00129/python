"""
from turtle import *  # turtle 모듈을 불러옵니다. 이 모듈은 그래픽을 그리기 위한 기능을 제공합니다.
#import turtle as t
color("blue", "pink")  # 터틀의 선 색을 'blue'로, 채우기 색을 'pink'로 설정합니다.
# 첫 번째 색은 선 색, 두 번째 색은 도형을 채울 때 사용할 색입니다.

begin_fill()  # 도형을 그리기 시작하면서 채우기를 시작합니다.
# 'begin_fill()'은 터틀이 그린 도형을 채우기 위해서 호출합니다.

while True:  # 무한 루프를 시작합니다. 이 루프는 특정 조건이 충족될 때까지 계속 실행됩니다.
    forward(200)  # 터틀이 현재 방향으로 200픽셀만큼 이동합니다.
    left(170)  # 터틀을 현재 방향에서 왼쪽으로 170도 회전시킵니다.
    if abs(pos()) < 1:  # 현재 위치가 (0, 0)에 매우 가깝다면, 즉 터틀이 시작 위치로 돌아왔으면
        break  # 루프를 종료합니다. (터틀이 원래 위치에 돌아왔음을 의미)
        
# pos() 함수는 현재 터틀의 위치를 (x, y) 형태로 반환하며, abs(pos())는 현재 위치의 원점으로부터의 거리입니다.
# 'if abs(pos()) < 1' 조건은 터틀이 시작 위치에 가까워졌는지를 확인하는 조건입니다.

end_fill()  # 도형을 다 그린 후 채우기를 완료합니다.

done()  # 터틀 그래픽을 끝내고, 창을 닫지 않고 유지하게 합니다.
# 'done()'은 그래픽 창을 유지시키고 종료를 의미합니다.
"""


